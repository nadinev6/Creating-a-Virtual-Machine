#!/bin/bash
# Enter VM name:
echo Name:
read VM-NAME
echo VM-NAME=$VM-NAME

# Enter Region:
echo Region:
read REGION
echo REGION=$REGION

# Enter Zone:
echo Zone:
read ZONE
echo ZONE=$ZONE

# Enter Machine type:
echo Machine type:
read MACHINE TYPE
echo MACHINE TYPE=$MACHINE TYPE

# Custom CPU type:
echo Custom CPU:
read CPU
echo CUSTOM-CPU=$CUSTOM-CPU

# Custom memory type:
echo Custom memory:
read CUSTOM-MEM
echo CUSTOM-MEM=$CUSTOM-MEM
